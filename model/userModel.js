const mongoose = require("mongoose");

const userSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    username: {
      type: String,
      required: true,
    },
    password: {
      type: String,
      required: true,
    },
    role: {
      type: String,
      default: 1,
    },
  },
  { timestamps: true }
);

const userModel = new mongoose.model("users", userSchema);
module.exports = userModel;
